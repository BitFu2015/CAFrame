//t0
void Timer0Event(void)
{
	uint8_t i,tmp;
	
	//LED_TOGGLE;
	
	tmp=(uint8_t)g_HolReg[0];
	for(i=0;i<8;i++)
	{
		if(tmp&0x01)
			drv_output(IO_Y1+i,1);
		else
			drv_output(IO_Y1+i,0);
		tmp>>=1;
		
		if(drv_input(IO_X1+i))
			g_HolReg[3]|=(1<<i);
		else
			g_HolReg[3]&=~(1<<i);
	}
	
	//drv_dac_output(IO_AO1,g_HolReg[1]);
	//drv_dac_output(IO_AO2,g_HolReg[2]);
	
	g_HolReg[4]=GetTemp(IO_AI1);
	g_HolReg[5]=GetTemp(IO_AI2);
}

