uint16_t mbCRC16(uint8_t * pucFrame, uint16_t usLen)
{
    uint8_t           ucCRCHi = 0xFF;
    uint8_t           ucCRCLo = 0xFF;
    int             iIndex;

    while( usLen-- )
    {
        iIndex = ucCRCLo ^ *( pucFrame++ );
        ucCRCLo = ucCRCHi ^ aucCRCHi[iIndex];
        ucCRCHi = aucCRCLo[iIndex];
    }
    return (((uint16_t)ucCRCLo) << 8) | ucCRCHi;
}

uint16_t BufToReg(uint8_t *buf)
{
  uint16_t ret=buf[0];
  ret<<=8;
  return ret+buf[1];
}

void RegToBuf(uint8_t *buf,uint16_t reg)
{
  buf[0]=reg>>8;
  buf[1]=reg;
}


//MODBUS 命令处理 函数
uint8_t mbCommand(uint8_t *buf,uint8_t len)
{
	uint8_t i,addr,size;
	uint16_t crc;
	
	if(buf[0]!=1)		//地址不符
		return 0;	
	
	if(mbCRC16(buf,len)) //校验
		return 0;	
		
	addr=BufToReg(buf+2);	
	size=BufToReg(buf+4);		
	if(buf[1]==0x03) 			//读多个寄存器
	{
		if(addr+size >6)  //无效地址和长度
			return 0;
		buf[2]=size*2; //数据长度
		len=3;
		for(i=0;i<size;i++)
		{
			RegToBuf(buf+len,g_HolReg[addr+i]);
			len+=2;
		}
		crc=mbCRC16(buf,len);
		RegToBuf(buf+len,crc);
		len+=2;
	}
	else if(buf[1]==0x10) //写多个寄存器
	{
		if(addr+size>3)		//数据地址和长度不符
			return 0;
		if(buf[6]!=size*2)	//写入长度不符
			return 0;
		for(i=0;i<size;i++)
			g_HolReg[addr+i]=BufToReg(buf+7+(i*2));
		crc=mbCRC16(buf,6);
		RegToBuf(buf+6,crc);
		len=8;
	}
	return len;
}

